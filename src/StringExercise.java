public class StringExercise {
    public static void main(String[] args) {
        String pinkFloyd = "We don't need no education\n" + "We don't need no thought control";
        System.out.println(pinkFloyd);

        String cashingChecks = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(cashingChecks);

        String cDrive = "In windows, the main drive is usually C:\\";
        System.out.println(cDrive);

        String backSlash = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.print(backSlash);

        String addme = "hello";
        System.out.println(addme + 5);
    }
}